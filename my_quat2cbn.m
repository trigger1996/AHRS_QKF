function cbn = quat2cbn(q)

    q11 = q(1)*q(1);
    q12 = q(1)*q(2);
    q13 = q(1)*q(3);
    q14 = q(1)*q(4);
    q22 = q(2)*q(2);
    q23 = q(2)*q(3);
    q24 = q(2)*q(4);
    q33 = q(3)*q(3);
    q34 = q(3)*q(4);
    q44 = q(4)*q(4);
    
    cbn(1,1) = q11 + q22 - q33 - q44;
    cbn(1,2) = 2*(q23 - q14);
    cbn(1,3) = 2*(q24 + q13);
    
    cbn(2,1) = 2*(q23 + q14);
    cbn(2,2) = q11 - q22 + q33 - q44;
    cbn(2,3) = 2*(q34 - q12);
    
    cbn(3,1) = 2*(q24 - q13);
    cbn(3,2) = 2*(q34 + q12);
    cbn(3,3) = q11 - q22 - q33 + q44;


end